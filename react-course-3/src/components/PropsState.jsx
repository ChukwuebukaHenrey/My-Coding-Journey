// Components are functions that returns something that is in a jsx format
//
// Components are reusable pieces of code that can stand alone and function independently
// components also have to start with a capital letter
// Components can be nested inside other components
// components hold private data called state that can be used to interact with other components
// components can also receive data from other components via props (properties)
// props are like arguments that are passed to a function
// props are read only and cannot be modified by the component that receives them
// state is like a variable that is declared inside a component
// state is mutable and can be modified by the component that owns it
// state is used to hold data that can change over time
// when state changes, the component re-renders to reflect the new state